    private void txtid_karyawanKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtid_karyawanKeyPressed
        if(evt.getKeyCode()== KeyEvent.VK_ENTER){
            if(txtid_karyawan.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "jamgan di kosongkan", "peringatan",JOptionPane.WARNING_MESSAGE);

            }else{
                txtussername.requestFocus();
            }

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtid_karyawanKeyPressed

