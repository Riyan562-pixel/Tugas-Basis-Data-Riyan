    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        login M = new login();

        M.setId_karyawan(Integer.parseInt(txtid_karyawan.getText()));
        M.setUssername(txtussername.getText());
        M.setpassword(txtshitf.getText());
        JOptionPane.showMessageDialog(null,"AKSES LOGIN BERHASIL DI TAMBAHAKAN");
        loginservices services = new loginservices();
        services.Insertlogin(M);
        dispose();
    }//GEN-LAST:event_btnsimpanActionPerformed

